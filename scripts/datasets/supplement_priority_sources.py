def process_text_sources(rows: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    results = []
    for entry in TEXT_SUPPLEMENT:
        out_path = out_path_for(entry)
        status, error = generate_text_pdf(entry, out_path)
        row = finalize_entry(entry, status, error, out_path)
        rows[row["id"]] = row
        results.append(row)
        print(f"{status:10s} {row['id']}")
    return results


def process_pdf_sources(rows: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    results = []
    for entry in PDF_SUPPLEMENT:
        out_path = out_path_for(entry)
        status, error = download_pdf(entry["source_url"], out_path)
        row = finalize_entry(entry, status, error, out_path)
        rows[row["id"]] = row
        results.append(row)
        print(f"{status:10s} {row['id']}")
    return results


def main() -> int:
    ensure_dirs()
    rows = load_sources()
    before_complete = len([row for row in rows.values() if row.get("download_status") in {"downloaded", "exists", "generated", "merged", "imported"}])
    results = process_text_sources(rows)
    results.extend(process_pdf_sources(rows))
    write_sources(rows)
    manifest = write_manifest_and_source_list(rows)
    stats = build_registry(rows)
    success = [row for row in results if row.get("download_status") in {"downloaded", "exists", "generated", "merged", "imported"}]
    summary = {
        "attempted": len(results),
        "successful": len(success),
        "failed": [row["id"] for row in results if row.get("download_status") == "failed"],
        "complete_before": before_complete,
        "complete_after": manifest["complete_documents"],
        "registry_stats": stats,
    }
    print(json.dumps(summary, ensure_ascii=False, indent=2, default=str))
    return 0 if not summary["failed"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
